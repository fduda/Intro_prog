import math  # Imports the math library


def cos_45():  # Defines the cos_45 function
    print(math.cos(45))  # Summons the cosine function, applies to it the value of 45 radians and prints the result


def natural_log_20():  # Defines the natural_log_20 function
    print(math.log(20))  # Summons the log function, applies to it the value of 20 and prints the result.
    # Since we only applied one value to the log function, it returns the natural log.


def triangle_area_3x12():  # Defines the triangle_area_3x12 function.
    print((12 * 3) / 2)  # Prints the result of the multiplication of 12 and 3, all divided by 2.
