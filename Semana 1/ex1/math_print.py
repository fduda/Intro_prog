import math  # Imports the math library


def cos_45():  # Defines the cos_45 function
    # Summons the cosine function, applies to it the value of 45 radians and prints the result
    print(math.cos(45))


def natural_log_20():  # Defines the natural_log_20 function
    # Summons the log function, applies to it the value of 20 and prints the result.
    # Since we only applied one value to the log function, it returns the natural log.
    print(math.log(20))



def triangle_area_3x12():  # Defines the triangle_area_3x12 function.
    # Prints the result of the multiplication of 12 and 3, all divided by 2.
    print((12 * 3) / 2)
